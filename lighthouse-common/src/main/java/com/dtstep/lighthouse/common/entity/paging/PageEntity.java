public final class PageEntity {

    private int currentPage;

    private int totalPage;

    private int totalSize;

    private int pageSize;

    private String baseUrl;

    private int startIndex;

    private int endIndex;

    public PageEntity(String baseUrl, int currentPage, int totalSize, int pageSize){
        this.baseUrl = baseUrl;
        this.currentPage = currentPage;
        this.totalSize = totalSize;
        this.pageSize = pageSize;
        totalPage = (totalSize + pageSize - 1) / pageSize;
        this.startIndex = (currentPage - 1) * pageSize + 1;
        this.endIndex = Math.min(currentPage * pageSize, totalSize);
    }

    public int getCurrentPage() {
        return currentPage;
    }

    public void setCurrentPage(int currentPage) {
        this.currentPage = currentPage;
    }

    public int getTotalPage() {
        return totalPage;
    }

    public void setTotalPage(int totalPage) {
        this.totalPage = totalPage;
    }

    public int getPageSize() {
        return pageSize;
    }

    public void setPageSize(int pageSize) {
        this.pageSize = pageSize;
    }

    public int getTotalSize() {
        return totalSize;
    }

    public void setTotalSize(int totalSize) {
        this.totalSize = totalSize;
    }

    public String getBaseUrl() {
        return baseUrl;
    }

    public void setBaseUrl(String baseUrl) {
        this.baseUrl = baseUrl;
    }

    public int getStartIndex() {
        return startIndex;
    }

    public void setStartIndex(int startIndex) {
        this.startIndex = startIndex;
    }

    public int getEndIndex() {
        return endIndex;
    }

    public void setEndIndex(int endIndex) {
        this.endIndex = endIndex;
    }
}
