import java.io.Serializable;

public enum AuthorizeStateEnum implements Serializable {

    AUTHORIZED_SUCCESS(1,"授权成功"),

    UNAUTHORIZED(2,"未授权"),

    AUTHORIZATION_EXPIRED(3,"授权已过期"),
    ;

    AuthorizeStateEnum(int state,String desc){
        this.state = state;
        this.desc = desc;
    }

    private int state;

    private String desc;

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
